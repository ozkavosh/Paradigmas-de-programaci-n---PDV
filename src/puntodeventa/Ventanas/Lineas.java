    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        String linea = txt_add_lin.getText();
        String desc_texto = txt_add_tdesc.getText();
        String desc_html = txt_add_hdesc.getText();
        String imagen = txt_add_img.getText();

        try {
            if(imagen.length() > 0){
              byte[] img_bytes = imgToBytes(imagen);
              if(img_bytes == null){
                JOptionPane.showMessageDialog(null, "Error al cargar la imagen!");
              }else{
                base.addLinea(linea, desc_texto, desc_html, img_bytes);  
              }  
            }else{
                base.addLinea(linea, desc_texto, desc_html);
            }
 
            tabla_lineas.setModel(base.getLineas());
            txt_add_img.setText(null);
        } catch (Exception e) {
            Logger.getLogger(Lineas.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btn_addActionPerformed

    private void tabla_lineasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_lineasMouseClicked
        if(tabla_lineas.getSelectedRow() != -1){
                btn_delete.setEnabled(true);
        }
        int row = tabla_lineas.getSelectedRow(); //Obtener fila seleccionada
         
        //Cargar datos en panel editar
        txt_edit_lin.setText(tabla_lineas.getValueAt(row, 0).toString());
        txt_edit_tdesc.setText(tabla_lineas.getValueAt(row, 1) == null ? "" : tabla_lineas.getValueAt(row, 1).toString());
        txt_edit_hdesc.setText(tabla_lineas.getValueAt(row, 2) == null ? "" : tabla_lineas.getValueAt(row, 2).toString());
        
        //Cargar datos en el panel borrar
        txt_delete_lin.setText(tabla_lineas.getValueAt(row, 0).toString());
        txt_delete_tdesc.setText(tabla_lineas.getValueAt(row, 1) == null ? "" : tabla_lineas.getValueAt(row, 1).toString());
        txt_delete_hdesc.setText(tabla_lineas.getValueAt(row, 2) == null ? "" : tabla_lineas.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tabla_lineasMouseClicked

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        String numero = txt_delete_lin.getText();
        
        int opc = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar el detalle?", "Eliminando detalle", JOptionPane.YES_NO_OPTION);
        
        if(opc == JOptionPane.YES_OPTION){
           try {
            base.deleteLinea(numero);
            tabla_lineas.setModel(base.getLineas());
            } catch (SQLException ex) {
            Logger.getLogger(Lineas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

