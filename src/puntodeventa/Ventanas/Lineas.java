    private void btn_borrar_imgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_borrar_imgActionPerformed
        if(tabla_lineas.getSelectedRow() != -1){
          try {
            int row = tabla_lineas.getSelectedRow();//Obtener fila seleccionada
            if(tabla_lineas.getValueAt(row, 3) != null){
                base.editLinea(tabla_lineas.getValueAt(row, 0).toString());
            }else{
                JOptionPane.showMessageDialog(null, "La linea seleccionada no tiene una imagen!", "Error eliminando la imagen", 1);
            }
            tabla_lineas.setModel(base.getLineas());
            } catch (Exception e) {
                Logger.getLogger(Pedidos.class.getName()).log(Level.SEVERE, null, e);
            }  
        }else{
         JOptionPane.showMessageDialog(null, "Debe seleccionar una linea!");   
        }
    }//GEN-LAST:event_btn_borrar_imgActionPerformed

