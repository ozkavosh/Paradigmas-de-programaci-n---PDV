    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Boolean flag = false;
        
        String numero = txt_edit_num.getText();
        String fec = sdf.format(txt_edit_fec.getDate());
        String rfec = sdf.format(txt_edit_rfec.getDate());
        String efec = "";
        if(txt_edit_efec.getDate() == null){
            flag = true;
        }else{
           efec = sdf.format(txt_edit_efec.getDate()); 
        }
        String estado = txt_edit_est.getText();
        String comentario = txt_edit_com.getText();
        String c_numero = txt_edit_cnum_test.getSelectedItem().toString();
        
        try {
            if(flag){
               base.editPedido(numero, fec, rfec, estado, comentario, c_numero); 
            }else{
               base.editPedido(numero, fec, rfec, efec, estado, comentario, c_numero); 
            }
            tabla_pedidos.setModel(base.getPedidos());
        } catch (SQLException ex) {
            Logger.getLogger(Pedidos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_editActionPerformed

