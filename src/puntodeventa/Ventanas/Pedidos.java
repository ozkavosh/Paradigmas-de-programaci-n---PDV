    private void tabla_pedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_pedidosMouseClicked
        try {
            if(tabla_pedidos.getSelectedRow() != -1){
                btn_delete.setEnabled(true);
            }
            int row = tabla_pedidos.getSelectedRow(); //Obtener fila seleccionada
            Boolean flag = false;
            
            String[] col = new String[7];
            for(int i = 0; i < col.length; i++){
                if(tabla_pedidos.getValueAt(row, i) != null){
                    col[i] = tabla_pedidos.getValueAt(row, i).toString();
                }else{
                    if(i == 3){
                        col[i] = "0000-00-00";
                        flag = true;
                    }else{
                       col[i] = ""; 
                    }
                }
            }
            
            //Cargar datos en panel editar
            txt_edit_num.setText(col[0]);
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(col[1]);
            txt_edit_fec.setDate(date);
            date = new SimpleDateFormat("yyyy-MM-dd").parse(col[2]);
            txt_edit_rfec.setDate(date);
            if(!flag){
              date = new SimpleDateFormat("yyyy-MM-dd").parse(col[3]);
              txt_edit_efec.setDate(date);
            }else{
              txt_edit_efec.setDate(null);  
            }
            txt_edit_est.setText(col[4]);
            txt_edit_com.setText(col[5]);
            txt_edit_cnum_test.setSelectedItem(col[6]);
            
            //Cargar datos en el panel borrar
            txt_delete_num.setText(col[0]);
            txt_delete_fec.setText(col[1]);
            txt_delete_rfec.setText(col[2]);
            txt_delete_efec.setText(col[3]);
            txt_delete_est.setText(col[4]);
            txt_delete_com.setText(col[5]);
            txt_delete_cnum.setText(col[6]);
        } catch (ParseException ex) {
            Logger.getLogger(Pedidos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tabla_pedidosMouseClicked

