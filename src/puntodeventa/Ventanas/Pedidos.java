    private void btn_enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_enviarActionPerformed
        if(tabla_pedidos.getSelectedRow() != -1){
          try {
            int row = tabla_pedidos.getSelectedRow();//Obtener fila seleccionada
            if(!tabla_pedidos.getValueAt(row, 4).toString().toLowerCase().contentEquals("shipped") && !tabla_pedidos.getValueAt(row, 4).toString().toLowerCase().contentEquals("cancelled")){
                base.editPedido(tabla_pedidos.getValueAt(row, 0).toString(),1);
            }else{
                String mensaje_error = tabla_pedidos.getValueAt(row, 4).toString().toLowerCase().contentEquals("shipped") ? "El pedido seleccionado ya fue enviado!" : "El pedido seleccionado fue cancelado!";
                JOptionPane.showMessageDialog(null, mensaje_error, "Error enviando el pedido", 1);
            }
            tabla_pedidos.setModel(base.getPedidos());
            } catch (Exception e) {
                Logger.getLogger(Pedidos.class.getName()).log(Level.SEVERE, null, e);
            }  
        }else{
         JOptionPane.showMessageDialog(null, "Debe seleccionar un pedido!");   
        }
        
    }//GEN-LAST:event_btn_enviarActionPerformed

