    private void tabla_pagosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_pagosMouseClicked
        try {
            if(tabla_pagos.getSelectedRow() != -1){
                btn_delete.setEnabled(true);
            }
            int row = tabla_pagos.getSelectedRow(); //Obtener fila seleccionada

            //Cargar datos en panel editar
            txt_edit_num_test.setSelectedItem(tabla_pagos.getValueAt(row, 0).toString());
            txt_edit_cnum.setText(tabla_pagos.getValueAt(row, 1).toString());
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse(tabla_pagos.getValueAt(row, 2).toString());
            txt_edit_fec.setDate(date);
            txt_edit_cant.setText(tabla_pagos.getValueAt(row, 3).toString());
            
            //Cargar datos en el panel borrar
            txt_delete_num.setText(tabla_pagos.getValueAt(row, 0).toString());
            txt_delete_cnum.setText(tabla_pagos.getValueAt(row, 1).toString());
            txt_delete_fec.setText(tabla_pagos.getValueAt(row, 2).toString());
            txt_delete_cant.setText(tabla_pagos.getValueAt(row, 3).toString());
        } catch (ParseException ex) {
            Logger.getLogger(Pagos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tabla_pagosMouseClicked

    private void btn_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_deleteActionPerformed
        String numero = txt_delete_cnum.getText();
        
        int opc = JOptionPane.showConfirmDialog(null, "Esta seguro de eliminar el pedido", "Eliminando pedido", JOptionPane.YES_NO_OPTION);
        
        if(opc == JOptionPane.YES_OPTION){
           try {
            base.deletePago(numero);
            tabla_pagos.setModel(base.getPagos());
            } catch (SQLException ex) {
            Logger.getLogger(Pagos.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btn_deleteActionPerformed

