    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            
            String numero = txt_edit_num_test.getSelectedItem().toString();
            String cnumero = txt_edit_cnum.getText();
            String fec = sdf.format(txt_edit_fec.getDate());
            String cantidad = txt_edit_cant.getText();
            
            if(Double.parseDouble(cantidad) <= 0){
                JOptionPane.showMessageDialog(null, "Ingrese una cantidad válida!", "Error!", 1);
            }else{
               base.editPago(numero, cnumero, fec, cantidad);
               tabla_pagos.setModel(base.getPagos()); 
            }
        } catch (SQLException ex) {
            Logger.getLogger(Pagos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btn_editActionPerformed

